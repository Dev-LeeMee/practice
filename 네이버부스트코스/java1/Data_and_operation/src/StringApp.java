
public class StringApp {

	public static void main(String[] args) {
		System.out.println("Hello world"); // String
		// System.out.println('Hello world'); // 자바에서 작은 따옴표는 character(문자1개)를 의미함.
		System.out.println('H'); // character
		System.out.println("H"); // string
		System.out.println("Hello \n world"); //new line(줄바꿈)
		System.out.println("Hello \"world\""); // 역슬래시(\) 뒤 따옴표는 문자열 따옴표라는 의
		
	}

}
